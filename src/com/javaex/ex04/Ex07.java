package com.javaex.ex04;

public class Ex07 {

	public static void main(String[] args) {
		
		int iTriangle = 7;
		
		for(int iCount = 1; iCount <= iTriangle; iCount++) {
			
			for(int i = 0; i < iCount; i++) {
				
				System.out.print("*");
			
			}
			
			System.out.println("");
		
		}
		
	}

}
